#!/bin/sh
# ДПЛ-5: контейнер стартует как root ТОЛЬКО чтобы привести владение data-тома и
# сбросить привилегии — сам релей (сетевой процесс с недоверенным вводом) работает
# под непривилегированным пользователем licno.
#
# Том /data на уже работающих серверах принадлежит root (раньше контейнер был root).
# chown делает переход на non-root безопасным И для новых, И для существующих томов:
# без него non-root не смог бы писать SQLite и релей упал бы на старте.
set -e
if [ "$(id -u)" = "0" ]; then
  chown -R licno:licno /data 2>/dev/null || true
  exec gosu licno "$@"
fi
exec "$@"
